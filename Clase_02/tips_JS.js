/*
las palabras reservadas "let" y "var" determinan que un contenedor puede variar, es decir puedo modificar su contenido
la palabra reservada "const" determina que un contenedor no puede variar, es decir no puedo modificar su contenido
*/


/*
cuando quiero escribir una cadena de texto, algun mensaje
pero en medio de dicho mensaje existen datos que pueden variar, tenemos dos formas de hacerlo
*/


let edad = 35;
let atura = 1.81;

console.log('mi edad es: ' + edad + ' y mi altura es: ' + atura);

//otra forma de hacerlo con template literals
// `` se llaman backsticks se pueden hacer con ALT + 96
console.log(`mi edad es: ${edad} y mi altura es: ${atura}`);


